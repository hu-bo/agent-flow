import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const protoPath = path.resolve(__dirname, '../proto/runner.proto');
const outputPath = path.resolve(__dirname, './runner.ts');

function parseEnum(proto, enumName) {
  const enumPattern = new RegExp(`enum\\s+${enumName}\\s*\\{([\\s\\S]*?)\\}`, 'm');
  const match = proto.match(enumPattern);
  if (!match) {
    throw new Error(`enum "${enumName}" not found in proto`);
  }

  const body = match[1];
  return [...body.matchAll(/([A-Z0-9_]+)\s*=\s*\d+\s*;/g)].map((m) => m[1]);
}

function toEngineUnion(enumMembers) {
  return enumMembers
    .filter((v) => v !== 'ENGINE_UNSPECIFIED')
    .map((v) => v.replace('ENGINE_', '').toLowerCase());
}

function toTaskEventTypeUnion(enumMembers) {
  return enumMembers
    .filter((v) => v !== 'TASK_EVENT_TYPE_UNSPECIFIED')
    .map((v) => v.replace('TASK_EVENT_TYPE_', '').toLowerCase());
}

function renderUnion(items) {
  return items.map((x) => `'${x}'`).join(' | ');
}

function main() {
  const proto = fs.readFileSync(protoPath, 'utf8');
  const engineMembers = parseEnum(proto, 'Engine');
  const taskEventTypeMembers = parseEnum(proto, 'TaskEventType');

  const engines = toEngineUnion(engineMembers);
  const taskEventTypes = toTaskEventTypeUnion(taskEventTypeMembers);

  const content = `// AUTO-GENERATED by protocol/types/generate-runner-types.mjs
// Source: protocol/proto/runner.proto
// DO NOT EDIT MANUALLY.

export type Engine = ${renderUnion(engines)};

export type TaskEventType = ${taskEventTypes
    .map((t) => `\n  | '${t}'`)
    .join('')};

export interface SandboxPolicy {
  enabled?: boolean;
  readOnly?: boolean;
  allowNetwork?: boolean;
  allowedWorkingDirs?: string[];
  allowedReadPaths?: string[];
  allowedWritePaths?: string[];
  blockedCommandFragments?: string[];
  allowedEnvKeys?: string[];
  deniedEnvKeys?: string[];
}

export interface DockerMount {
  source: string;
  target: string;
  readOnly?: boolean;
}

export interface DockerSpec {
  image: string;
  workDir?: string;
  user?: string;
  networkDisabled?: boolean;
  readOnlyRootFs?: boolean;
  mounts?: DockerMount[];
}

export interface TaskRequest {
  taskId: string;
  sessionId: string;
  stepId: string;
  command: string;
  args?: string[];
  env?: Record<string, string>;
  workingDir?: string;
  timeoutMs?: number;
  stream?: boolean;
  authToken?: string;
  engine?: Engine;
  sandboxPolicy?: SandboxPolicy;
  docker?: DockerSpec;
  inputJson?: unknown;
}

export interface CancelTaskRequest {
  taskId: string;
  reason?: string;
}

export interface CancelTaskResponse {
  accepted: boolean;
  message: string;
}

export interface HealthCheckResponse {
  status: string;
  version: string;
  unixTime: number;
}

export interface RunnerEventBase {
  taskId: string;
  sessionId: string;
  stepId: string;
  type: TaskEventType;
  timestamp: string;
  runnerId: string;
}

export interface StartedEvent extends RunnerEventBase {
  type: 'started';
  started: { message: string };
}

export interface StdoutEvent extends RunnerEventBase {
  type: 'stdout';
  stdout: { chunk: string };
}

export interface StderrEvent extends RunnerEventBase {
  type: 'stderr';
  stderr: { chunk: string };
}

export interface ProgressEvent extends RunnerEventBase {
  type: 'progress';
  progress: { message: string; percent: number };
}

export interface ResultEvent extends RunnerEventBase {
  type: 'result';
  result: { exitCode: number; outputJson: unknown };
}

export interface ErrorEvent extends RunnerEventBase {
  type: 'error';
  error: { message: string; retryable: boolean };
}

export interface CompletedEvent extends RunnerEventBase {
  type: 'completed';
  completed: { exitCode: number; durationMs: number };
}

export interface HeartbeatEvent extends RunnerEventBase {
  type: 'heartbeat';
  heartbeat: { message: string };
}

export type TaskEvent =
  | StartedEvent
  | StdoutEvent
  | StderrEvent
  | ProgressEvent
  | ResultEvent
  | ErrorEvent
  | CompletedEvent
  | HeartbeatEvent;
`;

  fs.writeFileSync(outputPath, content, 'utf8');
  process.stdout.write(`generated: ${outputPath}\n`);
}

main();
